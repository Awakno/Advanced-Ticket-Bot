class Color:
    # Définition des codes de couleur ANSI pour différentes couleurs
    red = "\033[91m"       # Rouge
    green = "\033[92m"     # Vert
    yellow = "\033[93m"    # Jaune
    blue = "\033[94m"      # Bleu
    magenta = "\033[95m"   # Magenta
    cyan = "\033[96m"      # Cyan
    white = "\033[97m"     # Blanc
    reset = "\033[0m"      # Réinitialisation de la couleur
    bold = "\033[1m"       # Gras
    underline = "\033[4m"  # Souligné
    end = "\033[0m"        # Fin de la couleur
